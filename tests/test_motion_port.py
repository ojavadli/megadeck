def test_get_origin_index_first_last_center() -> None:
    assert get_origin_index("first", 5) == 0
    assert get_origin_index("last", 5) == 4
    assert get_origin_index("center", 5) == 2.0
    assert get_origin_index("center", 6) == 2.5


def test_stagger_delay_first_origin() -> None:
    fn = stagger_delay(duration_s=0.1, origin="first")
    assert fn(0, 5) == pytest.approx(0.0)
    assert fn(2, 5) == pytest.approx(0.2)
    assert fn(4, 5) == pytest.approx(0.4)


def test_stagger_delay_last_origin_inverts_order() -> None:
    fn = stagger_delay(duration_s=0.1, origin="last")
    # last item has zero delay, first item has the most
    assert fn(4, 5) == pytest.approx(0.0)
    assert fn(0, 5) == pytest.approx(0.4)


def test_stagger_delay_center_ripples_outward() -> None:
    fn = stagger_delay(duration_s=0.1, origin="center")
    # 5 items → center is index 2; items 1+3 fire at +0.1, 0+4 at +0.2
    assert fn(2, 5) == pytest.approx(0.0)
    assert fn(1, 5) == pytest.approx(0.1)
    assert fn(3, 5) == pytest.approx(0.1)
    assert fn(0, 5) == pytest.approx(0.2)
    assert fn(4, 5) == pytest.approx(0.2)


def test_stagger_delay_with_easing_remaps() -> None:
    """When an easing is supplied, the *delays themselves* get eased.
    A back-out easing on a 5-shape stagger should make the early shapes
    fire faster (negative bezier overshoot) than linear."""
    linear = stagger_delay(duration_s=0.1, origin="first")
    eased = stagger_delay(duration_s=0.1, origin="first", easing="back-out")
    # Linear: index 1 fires at 0.1
    assert linear(1, 5) == pytest.approx(0.1)
    # Eased: should *not* be exactly 0.1.
    assert eased(1, 5) != pytest.approx(0.1, abs=1e-6)


def test_materialise_stagger_returns_list_of_delays() -> None:
    delays = materialise_stagger(4, duration_s=0.05)
    assert delays == pytest.approx([0.0, 0.05, 0.10, 0.15], abs=1e-9)


def test_materialise_stagger_zero_children() -> None:
    assert materialise_stagger(0) == []
