# ---------------------------------------------------------------------------
# Spring physics — sanity-check against expected behaviour
# ---------------------------------------------------------------------------

def test_spring_origin_zero_target_one() -> None:
    s = Spring()
    assert s.position(0) == pytest.approx(0.0, abs=1e-9)


def test_spring_settles_at_target() -> None:
    """A long-running default spring should settle near target=1."""
    s = Spring()
    assert s.position(5000) == pytest.approx(1.0, abs=0.05)


def test_spring_visualduration_default() -> None:
    """Motion's default visualDuration=0.3s, bounce=0.3 → specific stiffness/damping.

    Reference values computed with Motion's `getSpringOptions({ visualDuration: 0.3,
    bounce: 0.3 })`:
        root = 2π / (0.3 * 1.2) = 17.453
        stiffness = root² ≈ 304.6
        damping = 2 * 0.7 * √304.6 ≈ 24.4
    """
    s = Spring.from_visual_duration(0.3, bounce=0.3)
    assert s.stiffness == pytest.approx(304.6, rel=0.01)
    assert s.damping == pytest.approx(24.4, rel=0.02)


def test_spring_underdamped_behaviour() -> None:
    """A very low-bounce (high-overshoot) spring should oscillate above 1."""
    s = Spring.from_visual_duration(0.4, bounce=0.6)
    overshot = max(s.position(t) for t in range(50, 800, 50))
    assert overshot > 1.0


def test_spring_overdamped_behaviour() -> None:
    """A heavily-damped (negative-bounce ~ damping_ratio > 1) spring should
    monotonically approach the target without overshoot."""
    s = Spring(stiffness=100, damping=30, mass=1)  # damping ratio 1.5
    assert s.damping_ratio > 1
    last = -1.0
    for t in range(20, 2000, 20):
        cur = s.position(t)
        assert cur >= last - 1e-6
        last = cur


def test_spring_sample_returns_keyframes() -> None:
    s = Spring.from_visual_duration(0.3, 0.3)
    samples = s.sample(n_samples=20, duration_ms=600)
    assert len(samples) == 21
    assert samples[0].t_ms == 0.0
    assert samples[-1].t_ms == 600.0


def test_spring_from_duration_recovers_constants() -> None:
    """Spring.from_duration with the defaults should give back roughly the
    canonical spring constants."""
    s = Spring.from_duration(duration_ms=800, bounce=0.3)
    # Reference: Motion's findSpring({duration:800, bounce:0.3, mass:1, velocity:0})
    # converges on stiffness ≈ 75-90, damping ≈ 15-18 depending on the seed.
    assert 30 <= s.stiffness <= 200
    assert 5 <= s.damping <= 30


