def test_ease_in_uses_motion_constants() -> None:
    """Motion: easeIn = cubicBezier(0.42, 0, 1, 1)"""
    assert ease_in(0) == 0
    assert ease_in(1) == 1
    # easeIn should accelerate slowly — at t=0.5, y < t.
    assert ease_in(0.5) < 0.5


def test_ease_out_inverts_ease_in() -> None:
    # easeOut at any t > 0 should be > t (decelerates).
    for t in (0.25, 0.5, 0.75):
        assert ease_out(t) > t


def test_ease_in_out_is_symmetric() -> None:
    # easeInOut should map t and 1-t to mirrored values.
    assert ease_in_out(0.5) == pytest.approx(0.5, abs=0.01)


