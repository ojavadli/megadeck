def test_circ_in_zero_one() -> None:
    assert circ_in(0) == pytest.approx(0, abs=1e-9)
    assert circ_in(1) == pytest.approx(1, abs=1e-9)


def test_back_out_overshoots() -> None:
    """`back-out` should briefly exceed 1 before settling at 1."""
    overshot = max(back_out(t) for t in (0.6, 0.7, 0.8, 0.9))
    assert overshot > 1.0  # the canonical bounce


def test_back_in_pulls_back_first() -> None:
    """`back-in` should briefly go *below* 0 before climbing."""
    pulled = min(back_in(t) for t in (0.05, 0.1, 0.15, 0.2))
    assert pulled < 0.0


def test_anticipate_endpoints() -> None:
    assert anticipate(0) == pytest.approx(0, abs=1e-9)
    assert anticipate(1) == 1


