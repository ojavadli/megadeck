# ---------------------------------------------------------------------------
# Cubic-bezier — comment in Motion's source: cubicBezier(.17,.67,.83,.67)
# evaluated at 0.5 should be ≈ 0.627. Verify our port matches.
# ---------------------------------------------------------------------------

def test_cubic_bezier_matches_motion_reference() -> None:
    """Reference value from Motion's cubic-bezier.ts header comment."""
    ease_out_local = cubic_bezier(0.17, 0.67, 0.83, 0.67)
    assert ease_out_local(0.5) == pytest.approx(0.627, abs=0.005)


def test_cubic_bezier_endpoints() -> None:
    f = cubic_bezier(0.42, 0, 0.58, 1)
    assert f(0) == 0
    assert f(1) == 1


def test_cubic_bezier_linear_short_circuit() -> None:
    f = cubic_bezier(0.5, 0.5, 0.5, 0.5)
    for x in (0.0, 0.25, 0.5, 0.75, 1.0):
        assert f(x) == x


