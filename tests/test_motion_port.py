def test_reverse_easing_inverts() -> None:
    rev = reverse_easing(ease_in)
    for t in (0.1, 0.3, 0.5, 0.7, 0.9):
        assert rev(t) == pytest.approx(1 - ease_in(1 - t), abs=1e-9)


def test_mirror_easing_is_symmetric() -> None:
    m = mirror_easing(ease_in)
    # First half of easeInOut should equal half of easeIn at 2t.
    for t in (0.1, 0.25, 0.4):
        assert m(t) == pytest.approx(ease_in(2 * t) / 2, abs=1e-9)
    # Second half should mirror.
    for t in (0.6, 0.75, 0.9):
        assert m(t) == pytest.approx((2 - ease_in(2 * (1 - t))) / 2, abs=1e-9)


